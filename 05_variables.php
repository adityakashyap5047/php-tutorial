<?php 
    ECHO "keywords in php is not case sensitive";
    
    //Variable -> are containers for storing information
    // Starts with a $
    $name = "Aditya";
    $profile = "Developer";

    // We use <br> tag for line break
    echo "Heyyy, This is $name and he is a $profile <br><br>";

    // Variables in php are case sensitive
    $NAME = "Aditya Kumar";
    echo "Heyy, he is $name and his full name is $NAME <br><br>";

    // php is a loosely typed language
    $income = 100.5;
    echo "Heyy, he is $name and his full name is $NAME and his income is $income <br><br>";
?>